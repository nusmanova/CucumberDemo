package ArraysDemo;

public class ArraysDemo3 {

	public static void main(String[] args) {
		
		
		 Object n[]= new Object [3];

		 n [0]=35;
		 n [1]= "Nasiba";
		 n [2]= 'U';
		 
		 System.out.println(n[2]);
		 System.out.println(n[0]);
		 
		
		 
		 for (Object i:n)
		 {
			 System.out.println(i);
		 }

	}
	

}
