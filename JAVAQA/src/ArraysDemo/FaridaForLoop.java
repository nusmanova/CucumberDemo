package ArraysDemo;

public class FaridaForLoop {

	public static void main(String[] args) {
	
		
		for (int f=5; f<=25; f++) 
		{
			System.out.println(f);
		}
		
		
		
		for (int n=36; n>=10; n--) 
		{
			System.out.println(n);
		}

		
		for (int p=8; p<=16; p++) 
		{
			System.out.println(p);
		}
	}

}
