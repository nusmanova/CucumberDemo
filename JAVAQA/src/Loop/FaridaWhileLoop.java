package Loop;

public class FaridaWhileLoop {

	public static void main(String[] args) {
		
		
		int f= 5; // increment 
		
		while (f<=25) 
		
		{
          System.out.println(f);
          f++;
		}

		
		
		
		int b=10; //decrement 
		
		while (b>=1)
		{
			System.out.println(b);
	          b--;
		
	}
	

}
}